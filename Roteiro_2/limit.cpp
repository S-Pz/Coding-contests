#include <bits/stdc++.h>

int main() {
    std::cout << "Tamanho de int em bytes: " << sizeof(int) << std::endl;
    std::cout << "Valor máximo de int: " << std::numeric_limits<int>::max() << std::endl;
    std::cout << "Valor mínimo de int: " << std::numeric_limits<int>::min() << std::endl;
    return 0;
}